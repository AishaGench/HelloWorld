package homeWork;

public class ArithmeticOperators {
    public static void main(String[] args) {
        //Create two numeric variables and then exercise arithmetic operators (+. -, * , /, %)
        // for addition (+)
        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;
        System.out.println(sum);

        //for subtract (-)
        int num3 = 60;
        int num4 = 20;
        int sub = num3 - num4;
        System.out.println(sub);

        // for multiply (*)
        int num5 = 6;
        int num6 = 2;
        int mul = num5 * num6;
        System.out.println(mul);

        //for division
        int num7 = 6;
        int num8 = 2;
        int div = num7 - num8;
        System.out.println(div);

        //for remainder
        int num9 = 15;
        int num10= 4;
        int remain = num9% num10;
        System.out.println(remain);

    }
}
