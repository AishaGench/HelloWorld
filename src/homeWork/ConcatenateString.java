package homeWork;

public class ConcatenateString {
    public static void main(String[] args) {
        //Create two string variables and concatenate them using + operator.
        String str1 = "I like idioms";
        String str2 = "What's your favorite idiom";
        String word = str1 + " " + str2;
        System.out.println(word);
        System.out.println("================");
        System.out.println("I like idioms" + " " + "What's your favorite idiom");


    }
}
